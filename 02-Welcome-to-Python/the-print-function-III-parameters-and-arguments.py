# Parameter : A name for an expected argument to a function
# sep, end and file are keyword parameter as we have name them by the key
# which is sep, end and file

# Argument : The actual value that is passed as input to a function
# Sequential argument : Arguments are passed sequentially in order
# ( Eg for sequential argument is the *objects in print() function)

# Keyword argument: An argument whose parameter name is explicitly written out !!
# ( Eg for keyword argument is the sep and end in the print() function)

# Default Argument : Fallback value provided to a parameter if the user does not specify one.

print('Bangalore','was','a_garden_city', sep = '--*--')

print( 'Bangalore','still is', 'a garden city', end = '!!!!!')

print( 'Bangalore','still is', 'a garden city', sep ='--*--', end = '!!!!!')